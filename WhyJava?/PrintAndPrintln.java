public class PrintAndPrintln {
            public static void main(String[] args) {
            System.out.print("Hello, world! ");
            System.out.print("My name is Java.");
            System.out.print("I am a programming language. ");
            System.out.print("I am a platform.");
            System.out.print("I am a brand. ");
            System.out.print("I am a technology.");
            print();
            }

            public static void print() {
            System.out.println("Hello, world!");
            System.out.println("My name is Java.");
            System.out.println("I am a programming language.");
            System.out.println("I am a platform.");
            System.out.println("I am a brand.");
            System.out.println("I am a technology.");
            }
}
