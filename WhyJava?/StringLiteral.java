public class StringLiteral {
            public static void main(String[] args) {
            System.out.println("A string literal is a sequence of characters enclosed in double quotes.");
            System.out.println("A string literal can contain letters, digits, and special characters.");
            System.out.println("A string literal can be used to initialize a String variable.");
            System.out.println("A string literal can be passed as an argument to a method.");
            System.out.println("A string literal can be concatenated with other string literals.");
            stringLiteralExamples();
            }
            public static void stringLiteralExamples() {
            System.out.println("AP Computer Science A");
            System.out.println("124&$%##****");
            System.out.println("Java Programming" + " is fun!");
            System.out.println("Java Programming" + " is fun" +  " and" + " I love it!");
            }
}
